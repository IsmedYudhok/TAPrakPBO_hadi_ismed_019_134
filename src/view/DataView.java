    private void cariData(String key){
         model.getDataVector().removeAllElements();
        model.fireTableDataChanged();

        try{
            datastock.setModel(model);
            Connection c = Koneksi.connection();
            Statement s = c.createStatement();
            
            
            
            ResultSet r =s.executeQuery("SELECT * from stock WHERE kode_barang LIKE '%"+key+"%' OR nama_barang LIKE '%"+key+"%' OR jumlah LIKE '%"+key+"%'");  
            while(r.next()){
                Object[] data={
                    r.getString("kode_barang"),
                    r.getString("nama_barang"),
                    r.getString("jumlah"),
                    r.getString("kondisi"),
                            
                };
               model.addRow(data);
            }                
        } catch (Exception ex) {
        System.err.println(ex.getMessage());
        }
    }
    
 public final void loadData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();

        try {
            Connection c = Koneksi.connection();
            Statement s = c.createStatement();

            String sql = "SELECT * FROM stock";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                Object[] o = new Object[5];
                o[0] = r.getString("kode_barang");
                o[1] = r.getString("nama_barang");
                o[2] = r.getString("jumlah");
                o[3] = r.getString("kondisi");
                
                
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        }
    }
 
 
