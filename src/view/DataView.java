    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        if(txtkode.getText().equals("") ||txtnama.getText().equals("") || txtjumlah.getText().equals("")|| cmbkond.getSelectedItem().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "Warehouse Project", JOptionPane.INFORMATION_MESSAGE);
        }else{
        int i = datastock.getSelectedRow();
        if (i == -1) {
            return;
        }
        String user = (String) model.getValueAt(i, 0);
        try {
            Connection c = Koneksi.connection();
            String sql = "UPDATE  stock SET nama_barang =  '" + txtnama.getText() + "', jumlah='"+ txtjumlah.getText() + "', kondisi='"+ cmbkond.getSelectedItem() + "' WHERE  kode_barang ='" + txtkode.getText() + "'";
            PreparedStatement p = c.prepareStatement(sql);
            p.executeUpdate();
            p.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        } finally {
            loadData();
            txtkode.setText("");
            txtnama.setText("");
            txtjumlah.setText("");
             cmbkond.setSelectedItem("");
            JOptionPane.showMessageDialog(null, "Data berhasil diubah", "Warehouse Project", JOptionPane.INFORMATION_MESSAGE);
        }
        }

    }//GEN-LAST:event_btnupdateActionPerformed

    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
       if(txtkode.getText().equals("") ||txtnama.getText().equals("") || txtjumlah.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "Warehouse Project", JOptionPane.INFORMATION_MESSAGE);
        }else{
            String kodebarang = txtkode.getText();
            String namabarang = txtnama.getText();
            String jumlahbarang = txtjumlah.getText();
            String kondisibarang = (String) cmbkond.getSelectedItem();
           
            try {
               
                Connection c = Koneksi.connection();

                String sql = "INSERT INTO stock VALUES (?, ?, ?, ?)";

                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, kodebarang);
                p.setString(2, namabarang);
                p.setString(3, jumlahbarang);
                p.setString(4, kondisibarang);

                
                p.executeUpdate();
                p.close();
                
            } catch (SQLException e) {
                System.out.println("Terjadi Error");
            } finally {
                loadData();
                
                txtkode.setText("");
                txtnama.setText("");
                txtjumlah.setText("");
                cmbkond.setSelectedItem("");

                JOptionPane.showMessageDialog(null, "Data berhasil tersimpan", "Warehouse Project", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnsaveActionPerformed

